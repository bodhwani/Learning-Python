#Python that uses dictionary, set keys

# This dictionary contains key-value pairs.
dictionary = {"cat": 1, "dog": 2, "bird": 3}
print(dictionary)

# This set contains just the dictionary's keys.
keys = set(dictionary)
print(keys)
"""
Output

{'bird': 3, 'dog': 2, 'cat': 1}
{'cat', 'bird', 'dog'}
"""